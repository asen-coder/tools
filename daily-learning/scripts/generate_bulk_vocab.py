def generate_example(en, zh, pos):
    """生成例句"""
    templates = POS_TEMPLATES.get(pos, ["This is {word}."])
    template = random.choice(templates)

    if "{word}" in template:
        return template.replace("{word}", en)
    elif "{noun}" in template:
        noun = random.choice(["book", "car", "house", "tree", "man", "woman"])
        return template.replace("{noun}", noun).replace("{word}", en)
    else:
        return f"This is {en}."

def make_csv_row(en, zh, pos, definition, difficulty):
    """生成 CSV 行"""
    example = generate_example(en, zh, pos)
    return {
        "category": "general",
        "en": en,
        "zh": zh,
        "definition": definition,
        "example": example,
        "difficulty": difficulty,
    }

def main():
    rows = []

    for en, zh, pos, definition, difficulty in VOCABULARY:
        rows.append(make_csv_row(en, zh, pos, definition, difficulty))

    # 按难度排序
    rows.sort(key=lambda x: x["difficulty"])

    # 写入 CSV
    with open(OUTPUT_CSV, "w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=["category", "en", "zh", "definition", "example", "difficulty"],
        )
        writer.writeheader()
        writer.writerows(rows)

    print(f"✓ 已生成 {len(rows)} 条词汇到 {OUTPUT_CSV}")

    from collections import Counter
    by_diff = Counter(r["difficulty"] for r in rows)
    print(f"  难度分布:")
    for diff in sorted(by_diff):
        print(f"    难度 {diff}: {by_diff[diff]} 条")

    cats = Counter(r["category"] for r in rows)
    print(f"  分类分布:")
    for cat, count in cats.most_common():
        print(f"    {cat}: {count} 条")

if __name__ == "__main__":
    main()
