# 由于篇幅限制，这里只写了部分示例
# 实际应用中需要继续扩充到 5000 词
# 生成函数会自动为每个词汇添加难度标记和例句


def generate_example(en, zh, pos):
    """生成简单例句"""
    templates = POS_TEMPLATES.get(pos, ["This is {word}."])
    template = random.choice(templates)

    # 替换占位符
    if "{word}" in template:
        example = template.replace("{word}", en)
    elif "{noun}" in template:
        noun = random.choice(["book", "car", "house", "tree", "man", "woman"])
        example = template.replace("{noun}", noun).replace("{word}", en)
    else:
        example = f"This is {en}."

    return example


def make_csv_row(en, zh, pos, definition, difficulty):
    """生成 CSV 行"""
    # 决定分类：ai/programming/music/general/daily
    category = "general"
    if difficulty <= 2:
        category = "general"
    elif difficulty <= 3:
        category = "general"
    else:
        category = "general"

    example = generate_example(en, zh, pos)

    return {
        "category": category,
        "en": en,
        "zh": zh,
        "definition": definition,
        "example": example,
        "difficulty": difficulty,
    }


def main():
    rows = []

    # 处理不同难度的词汇
    all_words = DIFF_1 + DIFF_2

    for en, zh, pos, definition, difficulty in all_words:
        rows.append(make_csv_row(en, zh, pos, definition, difficulty))

    # 随机打乱
    random.shuffle(rows)

    # 写入 CSV
    with open(OUTPUT_CSV, "w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=["category", "en", "zh", "definition", "example", "difficulty"],
        )
        writer.writeheader()
        writer.writerows(rows)

    print(f"✓ 已生成 {len(rows)} 条词汇到 {OUTPUT_CSV}")
    print(f"  分类分布:")
    from collections import Counter
    cats = Counter(r["category"] for r in rows)
    for cat, count in cats.most_common():
        print(f"    {cat}: {count} 条")


if __name__ == "__main__":
    main()
